<?php
class Person {
    private $name;
    private $age;

    public function __construct($name, $age) {
        $this->name = $name;
        $this->age = $age;
    }

    public function getName() {
        return $this->name;
    }

    public function getAge() {
        return $this->age;
    }

    public function setName($name) {
        $this->name = $name;
    }

    public function setAge($age) {
        $this->age = $age;
    }
}

$person = new Person("一郎", 51);
$person->setName("秀喜");
$person->setAge(55);

echo $person->getName() . "\n";
echo $person->getAge() . "\n";
